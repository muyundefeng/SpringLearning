public class Haoyongapp implements PageProcessor{

	Site site = Site.me().setCharset("utf-8").setRetryTimes(PropertiesUtil.getRetryTimes()).
			setSleepTime(PropertiesUtil.getInterval());

	private Logger LOGGER = LoggerFactory.getLogger(Apk3.class);

	public Apk process(Page page) {
	
		if("http://android.haoyongapp.com/".equals(page.getUrl().toString()))
		{
			List<String> apps = page.getHtml().links("//div[@class='pageBody']").all();
			page.addTargetRequests(apps);
		}
	
		
	
		//提取页面信息
		if(page.getUrl().regex("http://android.haoyongapp.com/app/\\d+.html").match())
		{
			
			Apk apk = Haoyongapp_Detail.getApkDetail(page);
			
			page.putField("apk", apk);
			if(page.getResultItems().get("apk") == null){
				page.setSkip(true);
				}
			}
		else{
			page.setSkip(true);
			}
		return null;
	}
	@Override
	public List<Apk> processMulti(Page page) {
		// TODO Auto-generated method stub
		return null;
	}
	
	public Site getSite() {
		return site;
	}
}
